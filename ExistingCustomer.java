
class ExistingCustomer{
	private String customerClass,customerEmail,customerName;
	private int customerYearlyPurchaseAmt,loyaltyPoints;
	
	public String getCustomerClass() {
		return customerClass;
	}
	public void setCustomerClass(String customerClass) {
		this.customerClass = customerClass;
	}
	public String getCustomerEmail() {
		return customerEmail;
	}
	public void setCustomerEmail(String customerEmail) {
		this.customerEmail = customerEmail;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public int getCustomerYearlyPurchaseAmt() {
		return customerYearlyPurchaseAmt;
	}
	public void setCustomerYearlyPurchaseAmt(int customerYearlyPurchaseAmt) {
		this.customerYearlyPurchaseAmt = customerYearlyPurchaseAmt;
	}
	public int getLoyaltyPoints() {
		return loyaltyPoints;
	}
	public void setLoyaltyPoints(int loyaltyPoints) {
		this.loyaltyPoints = loyaltyPoints;
	}


}